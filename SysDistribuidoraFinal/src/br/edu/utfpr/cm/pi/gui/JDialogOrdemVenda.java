    private void abrirJanelaPesquisaOrdemDeVenda() {
        Util.abrirJDialogCentralizado(new JDialogPesquisarOrdemVenda(null, true));
        OrdemVenda ordem = (OrdemVenda) Data.hash.get("ordemVenda");
        if (ordem == null) {
            return;
        }
        Data.hash.remove("ordemVenda");
        preencherFormularioComDados(ordem);
    }

    private void apagarProdutoDaLista() {
        int linha = jTableProdutosVenda.getSelectedRow();
        if (linha != -1) {
            produtosVendaTemporario.remove(linha);
            carregarTabelaProdutos();
        }
    }

    private void cadastrarOrdemVenda() {
         if (validarCampos()) {
            OrdemVenda ordem = new OrdemVenda();

            if (new Util().validarID(jTextFieldCodigoOrdem.getText())) {
                ordem.setId(Integer.parseInt(jTextFieldCodigoOrdem.getText()));                
                data = new Util().stringToCalendar(jFormattedTextFieldDataVenda.getText());
                ordem.setDataVenda(data);
                dataVencimento = new Util().stringToCalendar(jFormattedTextFieldDataVendaVencimento.getText());
                ordem.setDataVencimento(dataVencimento);
                ordem.setCliente(cliente);
                ordem.setProdutos(produtosVendaTemporario);


            } else {
                data = new Util().stringToCalendar(jFormattedTextFieldDataVenda.getText());
                ordem.setDataVenda(data);
                dataVencimento = new Util().stringToCalendar(jFormattedTextFieldDataVendaVencimento.getText());
                ordem.setDataVencimento(dataVencimento);
                ordem.setCliente(cliente);
                ordem.setProdutos(produtosVendaTemporario);
            }


            try {
                TransactionManager.beginTransaction();
                DaoOrdemVenda dGP = new DaoOrdemVenda();

                dGP.persist(ordem);
                TransactionManager.commit();
                JOptionPane.showMessageDialog(rootPane, "Cadastrado com sucesso!");
                limparFormulario();
            } catch (Exception e) {
                TransactionManager.rollback();
                JOptionPane.showMessageDialog(rootPane, "A ordem de venda nÃ£o pode ser cadastrada" + e);
                System.out.println(e);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Verifique os erros antes de continuar!");
        }
    }

    private void limparFormulario() {
        jTextFieldCodigoOrdem.setText(null);
        jFormattedTextFieldDataVenda.setText(null);
        jTableProdutosVenda.setBackground(Color.white);
        jFormattedTextFieldDataVendaVencimento.setText(null);
        jFormattedTextFieldDataVendaVencimento.setBackground(Color.white);
        jTextFieldCliente.setText(null);
        jTextFieldCliente.setBackground(Color.white);
        cliente = new Cliente();
        produtosVendaTemporario = new ArrayList();
        carregarTabelaProdutos();
    }

    private void deletarOrdemVenda() {
         if (new Util().validarID(jTextFieldCodigoOrdem.getText())) {
            try {
                TransactionManager.beginTransaction();
                Integer id = Integer.parseInt(jTextFieldCodigoOrdem.getText());
                DaoOrdemVenda dE = new DaoOrdemVenda();
                OrdemVenda ordem = dE.retrive(id);
                dE.delete(ordem);
                TransactionManager.commit();
                limparFormulario();
                JOptionPane.showMessageDialog(rootPane, "Excluido com sucesso!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Erro ao excluir!");
                TransactionManager.rollback();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um item para excluir!");
        }
    }

    private void buscarCliente() {
        Util.abrirJDialogCentralizado(new JDialogPesquisaClienteSimples(null, true));
        cliente = (Cliente) Data.hash.get("cliente");
        if (cliente == null) {
            return;
        }
        Data.hash.remove("cliente");
        jTextFieldCliente.setText(cliente.getNome());
    }

    private void buscarProduto() {
        Util.abrirJDialogCentralizado(new JDialogPesquisarProduto(null, true));
        Produto produto = (Produto) Data.hash.get("produto");
        if (produto == null) {
            return;
        }
        Data.hash.remove("produto");
        produtosVendaTemporario.add(produto);
        carregarTabelaProdutos();
    }

    private boolean validarCampos() {
         int erros = 0;
        if (jTextFieldCliente.getText() == null || jTextFieldCliente.getText().equals("")) {
            jTextFieldCliente.setBackground(new Color(247, 169, 157));
            erros++;
        }
        if (produtosVendaTemporario == null || produtosVendaTemporario.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Insira pelo menos 1 produto!");
            erros++;
        }
        if(new Util().stringToCalendar(jFormattedTextFieldDataVenda.getText()) == null){
             jFormattedTextFieldDataVenda.setBackground(new Color(247, 169, 157));
            erros++;
        }
        if(new Util().stringToCalendar(jFormattedTextFieldDataVendaVencimento.getText()) == null){
             jFormattedTextFieldDataVendaVencimento.setBackground(new Color(247, 169, 157));
            erros++;
        }
        if (erros != 0) {
            return false;
        }
        return true;
    }

    private void carregarTabelaProdutos() {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{"CÃ³digo", "Nome", "Valor", "Quantidade", "Grupo"});

        if (produtosVendaTemporario != null) {
            for (Produto produto : produtosVendaTemporario) {
                model.addRow(new Object[]{produto.getId(), produto.getNome(), produto.getValor(), produto.getQuantidade(), produto.getGrupoDeProduto().getDescricao()});
            }
            jTableProdutosVenda.setModel(model);
            calcularvalorTotalProdutos(produtosVendaTemporario);
        }
        
    }

    private void calcularvalorTotalProdutos(List<Produto> produtos) {
        double valorTotal = 0.0;
        for (int i = 0; i < produtos.size(); i++) {
            
            valorTotal+= (produtos.get(i).getQuantidade()*produtos.get(i).getValor());
        }
        jTextFieldValorTotal.setText(String.valueOf(valorTotal));
    }

