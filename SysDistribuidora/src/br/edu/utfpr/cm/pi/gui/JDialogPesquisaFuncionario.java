    private void povoarTabelaFuncionario() {
         DaoFuncionario dGP = new DaoFuncionario();
        funcionarios = dGP.list();
        mostrarFuncionarios(funcionarios);
    }

    private void mostrarFuncionarios(List<Funcionario> funcionarios) {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{"C√≥digo", "Nome", "CPF"});

        if (funcionarios != null) {
            for (Funcionario funcionario : funcionarios) {
                model.addRow(new Object[]{funcionario.getId(), funcionario.getNome(), funcionario.getCpf()});
            }
            jTablePesquisarFuncionario.setModel(model);
        }
    }

    private void pesquisarFuncionario() {
        if(jTextFieldPesquisa.getText()==null){
            povoarTabelaFuncionario();
            return;
        }
        if(jComboBoxFiltro.getSelectedIndex()==0){
            DaoFuncionario dGP = new DaoFuncionario();
            funcionarios = dGP.listarComFiltro(" nome LIKE '%"+jTextFieldPesquisa.getText()+ "%'");
            mostrarFuncionarios(funcionarios);
        }else{
            DaoFuncionario dGP = new DaoFuncionario();
            funcionarios = dGP.listarComFiltro(" id='"+jTextFieldPesquisa.getText()+ "'");
            mostrarFuncionarios(funcionarios);
        }
    }

    private void editarFuncionario() {
        int linha = jTablePesquisarFuncionario.getSelectedRow();
        if(linha != -1){
            Funcionario funcionario = funcionarios.get(linha);
            Data.hash.put("funcionario", funcionario);
            dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma linha para editar!.");
            return;
        }
    }
